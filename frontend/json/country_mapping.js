export const COUNTRY_LIST = Object.entries(COUNTRY_MAPPING).map(
  ([name, code]) => ({ name, code }),
);

export function findCountryCode(countryName) {
  if (!countryName) return null;
  const lower = countryName.toLowerCase().trim();
  const exact = Object.entries(COUNTRY_MAPPING).find(
    ([name]) => name.toLowerCase() === lower,
  );
  if (exact) return { name: exact[0], code: exact[1] };
  const partial = Object.entries(COUNTRY_MAPPING).find(
    ([name]) =>
      name.toLowerCase().includes(lower) || lower.includes(name.toLowerCase()),
  );
  if (partial) return { name: partial[0], code: partial[1] };
  return null;
}
